/**
 * react-native-screens@4.23.0 ships `lib/module/index.js` with `export * from './types'`
 * but `lib/module/types.js` is missing, which breaks Metro web bundling.
 * Add a minimal ESM stub when absent (Android native code is unaffected).
 */
import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const target = path.resolve(
  __dirname,
  '../node_modules/react-native-screens/lib/module/types.js'
);

if (!fs.existsSync(path.dirname(target))) {
  process.exit(0);
}

if (!fs.existsSync(target)) {
  fs.writeFileSync(
    target,
    `// Auto-generated by scripts/patch-rn-screens-module-types.mjs (react-native-screens 4.23.x web fix)
export {};
`
  );
  console.log('[patch-rn-screens] wrote', target);
}
